export const platformConfig = (token: any) => {
  if (token?.platform) {
    let platform = token.platform
    let platformConfig = platform.split('_')

    // 这个时候应该是 Raydium Launpad 
    if (platformConfig.length > 1 && platform.length > 40 && !platformOptions[platform]?.icon) {
      platform = platformOptions[platformConfig[1]] ? platformConfig[1] : platformConfig[0]
    }
    
    return platformOptions[platform]
  }
  return false
}


export const format_hot_index = (token: any) => {
  token.m5_featured_index = calc_featured_index(token, '5m')
  token.h1_featured_index = calc_featured_index(token, '1h')
  token.hot = calc_is_hot(token.token_address)  
  token.hot_index = token?.hot?.length

  const pc = platformConfig(token)
  token.is_trench_token = !!token?.is_trench_token || token?.hot_index?.length >= 2 || pc?.type == 'trench' 
  token.is_meteora = pc?.is_meteora
  token.platform_icon = pc?.icon || all_support_chains[token.chain]?.logo    
  token.platform_name = pc?.label || all_support_chains[token.chain]?.full_name
  token.is_fake_pump = (token?.token_address?.includes('pump') && !['Pump', 'Pump AMM'].includes(pc?.label)) && !token.analysis_whale_signal_time && (Date.now() / 1000 - token.swap_begin_time < 6 * 30 * 24 * 60 * 60) && !token.is_top_token && !token.is_diamond_token
  token.is_fake_four = (token?.token_address?.includes('4444') && !['Four V1', 'Four V2', 'Four', 'Binance Four'].includes(pc?.label)) && !token.analysis_whale_signal_time && (Date.now() / 1000 - token.swap_begin_time < 6 * 30 * 24 * 60 * 60) && !token.is_top_token && !token.is_diamond_token
  token.is_fake_bonk = (token?.token_address?.includes('bonk') && !['Launpad', 'LetsBonk 1', 'LetsBonk 2', 'Labs'].includes(pc?.label)) && !token.analysis_whale_signal_time && (Date.now() / 1000 - token.swap_begin_time < 6 * 30 * 24 * 60 * 60) && !token.is_top_token && !token.is_diamond_token
  token.is_fake = (token.is_fake_pump || token.is_fake_four || token.is_fake_bonk)
}

export const calc_featured_index = (token: any, time_range: string) => {
  // 精选指数。只有 精选指数 >= 1 的才会出现在热门榜单
  let score = 0;
  //|| token.is_error_market_token
  if (token.price_change_1d < -80 || token.price_change_6h < -30 || (token.max_up_mcap * 0.3 > token.mcap) || token[`price_change_1d`] < -30) {
    return -10;
  }
  
  if (Date.now()/1000 - parseInt(token?.swap_begin_time) > 30 * 60 && (token.buyer_count_d1 + token.seller_count_d1) < 300) {
    return -10;
  }

  // 一天的狗，但是整体收益不超过10x 也不行
  if (Date.now()/1000 - parseInt(token?.swap_begin_time) < 24 * 60 * 60  && Date.now()/1000 - parseInt(token?.swap_begin_time) >  3 * 60 * 60 && token.price_change_1d < 1000) {
    return -10;
  }

  // 1小时价格 +30， +1分，价格跌 -30，-1分
  // 5分钟价格 +10， +1分，价格跌 -10， -1分
  // const time_range_map: any = { '5m': 10, '1h': 30 }
  // if (token[`price_change_${time_range}`] > time_range_map[time_range]) {
  //   score = score + 1
  // } 

  // if (token[`price_change_${time_range}`] < -time_range_map[time_range]) {
  //   score = score - 2
  // } 
  if (token[`price_change_1h`] > 70) {
    score = score + 1
  }   
  if (token[`price_change_5m`] > 30) {
    score = score + 1
  } 
  //有共振信号 + 1分
  if (store.degen_rankings[token.token_address]?.['whale_list']?.length >= 3) {
    score = score + 2
  }

  // 狙击+1
  if (!!token.analysis_whale_signal_time) {
    score = score + 1
  }

  // 是最近活跃的老狗 +1分
  if (Date.now()/1000 - parseInt(token?.swap_begin_time) < 24 * 60 * 60) {
    score = score + 0.5
  }  
  // if (token[`price_change_1h`] > 70) {
  //   score = score + 1
  // } 
  // if (token[`price_change_1h`] < -time_range_map['1h']) {
  //   score = score - 2
  // } 
  if (token[`price_change_1h`] < 10 || token[`price_change_6h`] < 10) {
    score = score - 2
  }

  // 如果目前从最高点回调了 50%，在度 -1
  if (token.max_up_mcap / 2 > token.mcap) {
    score = score - 2
  }

  if (token.is_meteora) {
    score = score - 1
  }

  if (token.is_fake) {
    score = score - 1
  }

  if (token.signal_max_ratio < 100) {
    score = score - 1.5
  }
  // if (token.max_up_mcap * 0.7 < token.mcap) {
  //   score = score + 1
  // }
  return score;
}


